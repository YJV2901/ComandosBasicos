package Calculadora;

//class de page
public class Operaçoes {

	
	
	public void soma(int x1, int x2) {
		
		int resultado = x1+x2; 
		System.out.println(" O resultado da sua soma é " +resultado);
	}
	
	public void divisão(int x1, int x2) {
		
		int resultado = x1/x2;
		System.out.println(" O resultado da sua divisão é " +resultado);
	}
	
	public void subtração(int x1, int x2) {
		
		int resultado = x1-x2;
		System.out.println("O resultado da sua subtração é " +resultado);
	}
	
	public void multiplicação(int x1, int x2) {
		
		int resultado = x1*x2;
		System.out.println("O resultado da sua subtração é " +resultado);
	}
	
	
}
